			<p><strong>Who is at risk of a fall at home?</strong></p>
			<br>
			<p>When it comes to falls the statistics are perhaps surprising. Most people don’t know that 1 in 3 people over 65 years have a substantial fall in any given year.<sup>1</sup></p>
			<br>
			<p>Falls are the leading cause of injury-related hospital admissions in people aged over 65.<sup>2</sup></p>
			<br>
			<p>60% of falls occur at home. They usually occur when getting out of a chair, bed or when going to the toilet.<sup>2</sup></p>
			<br>
			<p>The estimated average total hospital stay due to an injurious fall by an older person is 15 days.<sup>5</sup></p>
			<br>
			<p><strong>Frequent falling</strong></p>
			<br>
			<p>Those who fall are two to three times more likely to fall again.<sup>4</sup> The risk of falls increases proportionally with age. At 80 years, over half of seniors fall annually.</p>
			<br/><br/>
		</div>
		<div>
			<p><strong>Why do seniors fall?</strong></p>
			<br>
			<p>Older people are at increased risk because normal ageing often involves worsening balance, eyesight, muscle tone & slower reaction times.</p>
			<br>
			<p><strong>Unable to get up after falling</strong></p>
			<br>
			<p>For the elderly who fall and are unable to get up on their own, the period of time spent immobile often affects their health outcome. Muscle cell breakdown starts to occur within 30-60 minutes of compression due to falling. Dehydration, pressure sores, hypothermia, and pneumonia are other complications that may result.</p>
			<br>
			<p><strong>Getting help after a fall</strong></p>
			<br>
			<p>Getting help after an immobilizing fall improves the chance of survival by 80% and increases the likelihood of a return to independent living.</p>
