                    <h1 class="page-title meriweather"><span>Applications </span>for the Mobile Alarm.</h1>
                    <div>
                        <p>The <strong>Live<span style="color: #e40030;">Life</span></strong> Mobile Alarm is ideal for seniors but also has many applications across the community and workforce. Here are some of the situations the Mobile Alarm can be used in to provide not only safety but also freedom and peace of mind.</p>
                        <br>
                        <p><strong>Seniors who are still active and independent</strong></p>
                        <p>If you are a senior who remains active and goes out of your home unaccompanied, then the Mobile Alarm gives you the ability to take your emergency alarm with you. If you have a fall, whether at home or outdoors you can get help quickly by simply pressing the help button. Your emergency contacts will know where you are via the in-built GPS technology.</p>
                        <br>
                        <p><strong>Seniors living at home who don’t have an active landline</strong></p>
                        <p>There are many elderly people living alone who don’t have a landline at home. This is where the Mobile Alarm fills the gap. Getting a landline connected can be very costly for seniors and if you add on the cost of a landline alarm it can be a significant sum of money.</p>
                        <br>
                        <p><strong>Seniors who have a history of falling or who suffer from vertigo</strong></p>
                        <p>The Mobile Alarm can protect you if you are away from your house in the garden or out walking. It also has a fall detection option that can turned on that sends text messages to 5 emergency contacts when a person suffers a fall.</p>
                        <br>
                        <p><strong>People suffering from dementia and are at risk of wandering</strong></p>
                        <p>The Mobile Alarm comes with features that are ideal in ensuring the safety of people who are prone to wandering. Family and carers who know the mobile number assigned to the pendant can sent a text message to it and immediately find out their location on Google Maps.</p>
                        <br/>
                        <p>It also has a Geo-Fencing feature that can be set to send warning messages via text when the wearer of the pendant strays outside a defined radius (measured in metres). This is ideal for seniors living at home alone or even seniors in nursing homes and retirement villages.</p>
                        <br>
                        <p><strong>People suffering from disabilities</strong></p>
                        <p>Many people in our community suffer from disabilities or medical conditions that put them at risk. The Mobile Alarm can contact family and carers when a person suffering a disability needs help, no matter where they are.</p>
                        <br>
                        <p><strong>As a panic alarm when there is an intruder</strong></p>
                        <p>The Mobile Alarm can be used when the wearer suspects there may be an intruder or perhaps when walking alone outside and feeling unsafe or threatened.</p>
                        <br/>
                        <p><strong>Lone workers and to meet OH&S requirements</strong></p>
                        <p>Many workers are required to work alone outside or in remote locations. Some perform danderous tasks outside of normal business hours. The Mobile Alarm can be used to ensure the safety of lone workers. It also can help employers meet the requirements of OH&S regulations by ensuring workers have a quick, easy way to get help in an emergency by having the pendant call and text management or security staff when they need to.</p>
                        <br/>
                        <p><strong>Unaccompanied children</strong></p>
                        <p>Children who are unaccompanied or unsupervised whilst travelling are another application. Often children need to travel interstate or long distances by plane. A child can wear the pendant and a parent can track their exact location during their travel thereby ensuring they get from point A to point B without getting lost.</p>
                        <br/>
                        <p><strong>Bushwalkers and recreational staff</strong></p>
                        <p>The Mobile Alarm uses either the Spark or Vodafone Mobile Network and so has New Zealand’s widest coverage. It can be used by bushwalkers, tour guides and people in many remote locations in case of emergency. The emergency contacts will know the location to send help to via the help text message that displays it on Google Maps. To read all the Mobile Alarm <strong href="/order-mobile-alarm/features-in-detail/"><a>Features in detail click here</a></strong>.</p>
