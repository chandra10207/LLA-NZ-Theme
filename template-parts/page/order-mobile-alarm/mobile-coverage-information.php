                                <p>The Spark and Vodafone Mobile Coverage Maps are approximate only. Actual mobile alarm coverage depends on where you are and the device you are using. Customers should be aware that the mobile coverage maps displayed have been created using tools that predict the likely areas of coverage.</p>
