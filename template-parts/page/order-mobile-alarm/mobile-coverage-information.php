                <div class="mobile-tab-item" tab-id="bluetooth-disclaimer">
                    <div class="mobile-tab-header mobile-only">Bluetooth Disclaimer</div>
                    <div class="mobile-tab-content">
                        <div class="full-grid">
                            <div class="tab-item-left">
                                <p>Bluetooth is a technology that allows devices to communicate and share data over short distances only without wires. Bluetooth is great for when you are within a relatively short distance from your home such as outside in the backyard, next door at your neighbours or checking your mail box. Bluetooth location provides an accurate and fast solution to your location during an emergency in and around the vicinity of your home only.</p>
							<br>
							<p>Inside the charging station (or Home Beacon) that you received with your alarm is a Bluetooth 5.0 beacon. Your address can be pre-programmed into the Home Beacon by inputting the latitude and longitude co-ordinates of your home and we pair (or connect) the alarm to the Bluetooth Beacon so that the 2 devices are always connected.</p>
							<br>
							<p>Bluetooth (sometimes referred to as BLE by us) also has less power consumption than other location tools, such as GPS and/or wifi. This is one way we are able to get the Live Life Alarm to last as long as 4-5 days between recharges. Our testing indicates that Bluetooth can be relied upon to provide fast positioning when the alarm is within 25 metres of the Bluetooth beacon (Home Beacon). Once the alarm is outside this range, the Bluetooth capability will not work. However, the alarm will attempt to obtain location via GPS or wifi, which are great alternatives.</p>
