                <div class="mobile-tab-item" tab-id="gps-disclaimer">
                    <div class="mobile-tab-header mobile-only">GPS Functions Disclaimer</div>
                    <div class="mobile-tab-content">
                        <div class="full-grid">
                            <div class="tab-item-left">
                                <p>The LiveLife Mobile Alarm has built-in GPS & GLONAS technology that it uses to fix the location of the pendant with an accuracy of 2.5 metres. In order for the GPS functions to work it requires a line-of-sight connection to satellites in the sky. For the pendant to receive the signal it needs to be pointed to the sky. It normally takes a few seconds to receive the signal and work out the co-ordinates of it’s exact location. It can take up to 10 minutes to get it’s first fix out of the box. Please bear in mind though that the 4GX Live Life Alarm also attempts to obtain location using wifi signal, which is a great solution for when the alarm is out of GPS range but in a relatively built up area.</p>
							<br>
							<p>In the event that the pendant cannot receive the GPS signal it will revert to showing it’s last known position. This is the location of the device when it was last able to fix it’s location by satellite. The device conserves power by only trying to get a GPS fix when the device is moved. Because of this we cannot guarantee that when the device is activated the location given is always the current position at the time of activation or that when someone tries to locate it via text message that it will know it’s current position. With that said, if the device does not know it’s current position it will report it’s last known position. This usually means the alarm has not moved significantly from it’s last known position so in most cases this is actually the true location of the Mobile Alarm.</p>
							<br>
							<p>Underground carparks, lifts, concrete buildings, tunnels and other constructions can block the signal from satellites to the pendant. It is possible that if a wearer of the pendant is in a location where there is no GPS signal that there may also be no 4GX, 4G or 3G mobile signal meaning the location function of the pendant will not operate till the wearer moves to an unobstructed location. However, the automatic wifi location of the alarm will normally be shown in these circumstances rather then GPS position.</p>
