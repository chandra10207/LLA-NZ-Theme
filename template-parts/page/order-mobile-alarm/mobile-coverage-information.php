                <div class="mobile-tab-item" tab-id="wifi-disclaimer">
                    <div class="mobile-tab-header mobile-only">Wi-Fi Tracking Disclaimer</div>
                    <div class="mobile-tab-content">
                        <div class="full-grid">
                            <div class="tab-item-left">
                                <p>Wi-Fi location tracking is a geolocation system that uses the entire Wi-Fi infrastructure (phones, tablets, laptops, and routers) as Wi-Fi access points to determine a device’s location. Wi-Fi location tracking is a good system for geolocation whenever the Global Positioning System (GPS) can’t get the job done. For example, GPS can’t always locate your device when you’re indoors, as the signal coming from GPS satellites may be weak or easily blocked. Wifi works by comparing the list of Wi-Fi networks near you to a known list of access points and their locations. In your Live Life Alarm is a wifi chip module and in the case of an emergency, the Live Life Alarm will look for wifi access points to calculate your position. If a wifi location solution can be found faster than using GPS, your wifi location will be reported on Google Maps to your emergency contacts. However, the alarm will always look for a Bluetooth location solution first.</p>
							<br>
							<p>If you find yourself in an area such as a rural setting or out in the country, there is likely to be less wifi access points than in a built up area (such as cities and metropolitan areas). In cases such as these, the Live Life Alarm will more likely resolve to a GPS position for your location.</p>
							<br>
							<p>In the remote possibility a wifi position is reported whilst you are in a rural or country setting,  it is possible the accuracy of your location could be less than ideal.</p>
