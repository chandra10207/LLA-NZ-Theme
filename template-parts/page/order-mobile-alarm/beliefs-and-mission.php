            <h1 class="page-title"><span>Our Beliefs </span>& Mission.</h1>
            <div class="sidebar-grid">
                <div class="content-bar">
                    <div>
                        <p><strong>Background</strong></p>
                        <p>We’re a bunch of like minded people who came together after similar experiences of dealing with serious falls of loved ones and the often terrifying repercussions that followed.</p>
                    </div>
                    <br>
                    <div>
                        <p><strong>Beliefs</strong></p>
                        <p>We believe that people should Live the Life they deserve. We believe that people at risk should be able to control their own destiny following an emergency situation such as a fall. We believe that there is no good reason to pay for monitoring by a third party when those who love us the most are perfectly capable of coming to our aid. We also believe that the time taken to assist someone who needs help is directly proportional to the outcome.</p>
                        <br>
                        <p>Our dreams, hopes, aspirations and desires are to improve people’s quality of life. Our mobile alert product and customer service will positively impact the lives of our customers and their loved ones. We will be affordable, durable, highly functional, and be highly competitive. By doing so, we will have as many of our products being used by as many people as possible. We will attract and empower individuals with integrity who believe in what we are doing to join our team so that we can rise to our full potential and have as many people as possible LiveLife 24/7.</p>
                        <br>
                        <p>We will give those who have need a superior product that will help them live life without fear, without terror. We’ve heard too many stories and shared in too many experiences of those that have fallen. We think about the terror, hopelessness and embarrassment that they have felt. Many think that as they are on the ground for hours and hours, that their time is up. They are hungry and thirsty, maybe have to go to the bathroom, often in severe pain but are helpless. We think of what they are feeling. We want to do everything we can so they don’t have to go through that experience at all and have everything they need to feel protected – no matter where they are. We have worked hard to create something unique that deals with all facets of these situations.</p>
                    </div>
                    <br>
                    <div>
                        <p><strong>Mission</strong></p>
                        <p>We want everyone to have a Live Life Alarm mobile medical pendant where they can have more independence, while giving them peace of mind that help is just a press of a button away. We want to keep our products affordable. We want to bring families closer together. We want to give families peace of mind. We want people to feel safe and secure wherever they are. We want to extend the time people can live independently. We want to save lives. <strong>We want people to live the life they deserve. This is our WHY.</strong></p>
                    </div>
