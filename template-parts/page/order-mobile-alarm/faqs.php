                                </div>
                                <div class="accordion-content">
                                    <p>The pendant has a built-in fall detection feature that is designed to detect unbroken falls or the kind of fall more likely to result in unconsciousness. It measures the impact, angle and speed of a fall in order to ascertain if a fall has probably occurred. When it detects a fall the pendant will announce in a loud, clear voice “Fall has been detected, click the SOS/Heart button to cancel the fall alert sequence”. This will be repeated twice before sending out ‘fall down alert’ text messages to the programmed emergency contacts and the alarm will then start calling them. Fall detection technology is not perfect and false alarms can occur, however, the wearer will be given the chance to cancel the fall alert if it is a false alarm. Fall detection can be turned off by sending a simple text command to the pendant from a mobile phone or the fall detection can be made less or more sensitive by the user. The LiveLife Alarm cannot detect every fall, as every fall is different. For example, It may not be activated automatically if someones fall is blocked by an object, they brace themselves as they fall or perhaps if they slump or roll to the floor. If you are conscious and able to, it is recommended to press the personal alarm manually, with one short, sharp press. In your user manual you will find instructions on how to increase or decrease the sensitivity of the fall detection on your LiveLife Alarm. You can read more about how fall detection works by <strong><a href="/understanding-how-fall-detection-works/">clicking here.</a></strong></p>
