                        <p>Your current 3G model Live Life Alarm will continue to operate until Telstra disconnect 3G in mid-2024. <strong><a href="/product/upgrade/">Consider upgrading</a></strong> to the latest model before then. In the meantime, we encourage you to continue to do a <strong><a href="/wp-content/uploads/LiveLife-Alarms-Monthly-Health-Check.1.pdf">Monthly Health Check</a></strong> on your Live Life Alarm to make sure it’s working normally.</p>
