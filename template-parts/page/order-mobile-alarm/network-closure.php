                        <p><strong>Telstra 3G mobile network will stop working in mid-2024</strong></p>
                        <p>For most of Live Life Alarms customers there is no action required. However, if you have a 3G device (most often supplied before mid 2020) then you’ll need to upgrade to a 4G device before then. You’ve probably seen a few emails from us already that encourage you to consider an upgrade. Over the next couple of years we’ll make sure all 3G Live Life Alarms customers understand that their 3G alarm won’t work forever and that an upgrade might be a smart idea.</p>
